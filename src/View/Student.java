    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String indexNumbertxt = indexnumber.getText();
        String firstName = firstname.getText();
        String lastName = lastname.getText();
        String degree = jComboBox1.getSelectedItem().toString();
        String email = studentemail.getText();
        String phone = phonetxt.getText();

        if (indexNumbertxt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Index number is required", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int indexNumber;
        try {
            indexNumber = Integer.parseInt(indexNumbertxt);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Index number must be an integer", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean success = controller.updateStudent(indexNumber, firstName, lastName, degree, email, phone);
        
        if (success) {
            JOptionPane.showMessageDialog(this, "Student updated successfully!");
            populateStudentTable(); // Refresh the table after updating a student
            clearField(); // Clear fields after successful update
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update student!", "Error", JOptionPane.ERROR_MESSAGE);
        }
         
    
    }//GEN-LAST:event_jButton2ActionPerformed

