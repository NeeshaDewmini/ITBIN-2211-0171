    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String indexNumber = indexnumber.getText();
    
    
    int index = Integer.parseInt(indexNumber);
    
    // Confirm before deleting
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        boolean success = controller.deleteStudent(index);
        if (success) {
            JOptionPane.showMessageDialog(this, "Student deleted successfully!");
            populateStudentTable(); // Refresh the table after deletion
            clearField(); // Clear fields after successful deletion
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete student!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String indexNumber = searchbar.getText().trim();
            
            if (!indexNumber.isEmpty()) {
                // Call search method in StudentController
                StudentTable student = controller.searchStudentByIndex(indexNumber);
                
                if (student != null) {
                    // Populate text fields with found student data
                    indexnumber.setText(student.getIndexNumber());
                    firstname.setText(student.getFirstName());
                    lastname.setText(student.getLastName());
                    jComboBox1.setSelectedItem(student.getDegree());
                    studentemail.setText(student.getEmail());
                    phonetxt.setText(student.getPhone());
                    
                    // Optionally, notify user or update UI after successful search
                    JOptionPane.showMessageDialog(null, "Student found!");
                } else {
                    // Clear text fields or show message indicating student not found
                    clearField();
                    JOptionPane.showMessageDialog(null, "Student not found!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please enter an index number to search.");
            }
    
    }//GEN-LAST:event_jButton4ActionPerformed

