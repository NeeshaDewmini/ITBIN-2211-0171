    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String indexNumber = indexnumber.getText();
    String firstName = firstname.getText();
    String lastName = lastname.getText();
    String degree = jComboBox1.getSelectedItem().toString();
    String email = studentemail.getText();
    String phone = phonetxt.getText();

    if (controller.checkIfIndexNumberExists(indexNumber)) {
        JOptionPane.showMessageDialog(this, "Index number already exists!", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        boolean success = controller.addStudent(indexNumber, firstName, lastName, degree, email, phone);
        if (success) {
            JOptionPane.showMessageDialog(this, "Student added successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add student!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } populateStudentTable();
}

